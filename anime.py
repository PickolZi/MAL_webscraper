class Anime():
    def __init__(self, rank, title, anime_type, num_of_episodes, release_date, members, score, link):
        self.rank = rank
        self.title = title
        self.anime_type = anime_type
        self.num_of_episodes = num_of_episodes
        self.release_date = release_date
        self.members = members
        self.score = score
        self.link = link